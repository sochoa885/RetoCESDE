            <h2 class="card-title">Más de 28 millones de casos de covid-19 en el mundo</h2>
            <p class="card-text">Más de 28 millones de personas en todo el mundo han sido infectadas con el nuevo coronavirus, según la Universidad Johns Hopkins.

              Un pronóstico conjunto de los CDC ahora proyecta que habrá hasta 217.000 muertes por covid-19 en EE. UU. para el 3 de octubre.
              Un influyente modelo de coronavirus actualizó su pronóstico para enero, anticipando un escenario «más probable» de 415.090 muertes en Estados Unidos durante ese mes.


              «La nueva proyección de 415.000 muertes en Estados Unidos para el 1 de enero representa más del doble de las muertes en los últimos seis meses», dijo el Instituto de Medición y Evaluación de la Salud (IHME, por sus siglas en inglés) de la Universidad de Washington, en su último pronóstico.

              En el peor de los casos, los fallecimientos podrían llegar a más de 600.000 para el 1 de enero, dijo el IHME.

              Según la Universidad Johns Hopkins, se han registrado casi 6,4 millones de casos de covid-19 en EE.UU. y han muerto más de 191.000 estadounidenses. En el mejor de los casos planteados por el IHME, poco menos de 300.000 personas morirán.

              «Pero el uso de las máscaras en EE.UU. va en la dirección equivocada. Actualmente, sólo seis estados tienen un 50% de uso de mascarillas: California, Texas, Virginia, Hawaii, Florida y Alaska», dijo el IHME.</p>
